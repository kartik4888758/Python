import math

num = 64
sqrt = math.sqrt(num)
print(f"Square root of {num} is {sqrt}")

float_num = 3.98765
rounded_num = round(float_num, 2)
print(f"Rounded number: {rounded_num}")

neg_num = -15
abs_val = abs(neg_num)
print(f"Absolute value of {neg_num} is {abs_val}")

nums = [22, 33, 14, 42, 95]
max_val = max(nums)
min_val = min(nums)
print(f"Maximum value in the list: {max_val}")
print(f"Minimum value in the list: {min_val}")

n = 6
factorial = math.factorial(n)
print(f"Factorial of {n} is {factorial}")

base = 3
exponent = 4
power = math.pow(base, exponent)
print(f"{base} raised to the power of {exponent} is {power}")

angle = math.pi / 6
sin_val = math.sin(angle)
cos_val = math.cos(angle)
print(f"Sine of {angle} is {sin_val}")
print(f"Cosine of {angle} is {cos_val}")
