num1 = 7
num2 = 3
result_and = num1 & num2
print(f"Bitwise AND: {result_and}")

result_or = num1 | num2
print(f"Bitwise OR: {result_or}")

result_xor = num1 ^ num2
print(f"Bitwise XOR: {result_xor}")

result_not = ~num1
print(f"Bitwise NOT for num1: {result_not}")

shift_left = num1 << 2
print(f"Left Shift num1 by 2: {shift_left}")

shift_right = num1 >> 1
print(f"Right Shift num1 by 1: {shift_right}")
